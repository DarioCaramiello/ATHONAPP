
function change_border_on() {
    let x = document.getElementById("login_id")
    x.style.borderRadius = "40px"
    x.style.backgroundColor = "black"
    x.style.color = "white"
}

function change_border_off() {
    let x = document.getElementById("login_id")
    x.style.borderRadius = "0"
    x.style.backgroundColor = "#ffffff"
    x.style.color = "black"
}